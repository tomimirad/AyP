public class tiposDatos {
    public static void main(String[] args){
        System.out.println(616 + " es un dato tipo entero");
        System.out.println(613.0 + " es un dato tipo decimal");
        System.out.println("613" + " es un dato tipo texto");
        System.out.println("4.32" + " es un dato tipo texto");
        System.out.println(3.012e-12 + " es un dato tipo decimal");
        System.out.println(3+7 + " es un dato tipo entero");
        System.out.println(3.5+6.5 + " es un dato tipo decimal");


    }
}
