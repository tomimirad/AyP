public class operaciones {
    public static void main(String[] args){
        int x = 300;
        int y = 900;
        //suma
        System.out.println(x+y);
        //resta
        System.out.println(x-y);
        //multiplicacion
        System.out.println(x*y);
        //division
        System.out.println(y/x);
        //intercambio
        System.out.println(x=y);

    }
}
